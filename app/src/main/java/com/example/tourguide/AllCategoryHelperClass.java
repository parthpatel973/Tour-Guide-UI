package com.example.tourguide;

public class AllCategoryHelperClass {
    int image;
    String title;

    public AllCategoryHelperClass(int image, String title) {
        this.image = image;
        this.title = title;
    }

    public int getImage() {
        return image;
    }

    public String getTitle() {
        return title;
    }
}
